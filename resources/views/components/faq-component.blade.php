<script>
    $(document).ready(function() {
    // Toggle answers and change icon
    $('.question-title').click(function() {
        var answer = $(this).siblings('.answer');
        var icon = $(this).find('i');

        answer.slideToggle(function() {
            if (answer.is(':visible')) {
                icon.removeClass('fa-chevron-down').addClass('fa-chevron-up');
            } else {
                icon.removeClass('fa-chevron-up').addClass('fa-chevron-down');
            }
        });
    });

    // Show default topic content and focus default topic button
    var defaultTopicId = 1; 
    var defaultTopicContent = $('.topicContent[data-topic="' + defaultTopicId + '"]');

    $('.topicContent').not(defaultTopicContent).hide();
    $('.topic button').removeClass('active');

    defaultTopicContent.show();
    $('.topic button[data-topic="' + defaultTopicId + '"]').addClass('active').focus();

    // Toggle topic content
    $('.topic button').click(function() {
        var topicId = $(this).data('topic');
        var topicContent = $('.topicContent[data-topic="' + topicId + '"]');

        // Hide other topic contents and reset button backgrounds
        $('.topicContent').not(topicContent).hide();
        $('.topic button').removeClass('active');

        topicContent.slideToggle();
        $(this).toggleClass('active');
        
        // Reset all chevron icons to "down" and set the clicked one to "up"
        $('.fa-chevron-down').removeClass('fa-chevron-up');
        if (topicContent.is(':visible')) {
            $(this).find('.fa-chevron-down').addClass('fa-chevron-up');
        }
    });
});
</script>